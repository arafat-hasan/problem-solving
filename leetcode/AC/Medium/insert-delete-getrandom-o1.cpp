class RandomizedSet {
 public:
  RandomizedSet() {}

  bool insert(int val) {
    if (hash.find(val) != hash.end()) {
      return false;
    }

    hash[val] = list.size();
    list.push_back(val);
    return true;
  }

  bool remove(int val) {
    if (hash.find(val) == hash.end()) {
      return false;
    }

    swap(list[hash[val]], list[list.size() - 1]);
    hash[list[hash[val]]] = hash[val];
    list.pop_back();
    hash.erase(val);
    return true;
  }

  int getRandom() { return list[rand() % list.size()]; }

 private:
  map<int, int> hash;
  vector<int> list;
};


int main() {
    __FastIO;
    int m, len, start, end, cnt, ans;
    string str;
    map<char, int> mp;
    while (cin >> m and m) {
        cin.ignore();
        getline(cin, str);
        len = (int) str.size();
        start = 0; end = 0; cnt = 0; ans = -INF;
        while (end < len) {
            mp[str[end]]++;
            if (mp[str[end]] == 1) cnt++;
            end++;
            while (cnt > m and start <= end) {
                mp[str[start]]--;
                if (mp[str[start]] == 0) cnt--;
                start++;
            }
            ans = max(ans, end - start);
        }
        cout << ans << '\n';
        mp.clear();
    }
    return 0;
}


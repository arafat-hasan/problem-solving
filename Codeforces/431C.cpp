int n, k, d, node;
int dp[105][2];

int dfs (int path, bool fl) {
    if (dp[path][fl] != -1) return dp[path][fl];

    if (path == 0) return fl;

    int tmp = 0;

    for (int i = 1; i <= k; i++) {
        if ( (fl and path - i >= 0) )
            tmp = (tmp + dfs (path - i, true) % MOD) % MOD;
        else if (path - i >= 0) {
            tmp = (tmp + dfs (path - i, i >= d) % MOD) % MOD;
        }
    }

    return dp[path][fl] = tmp;
}


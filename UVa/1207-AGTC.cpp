int dp[2010][2010];
char X[2010], Y[2010];

int lcs(int m, int n ) {
    if (m == -1) return n + 1;
    if (n == -1) return m + 1;
    if (dp[m][n] != -1) return dp[m][n];
    if (X[m] == Y[n])
        dp[m][n] = lcs(m - 1, n - 1);
    else
        dp[m][n] = min(lcs(m - 1, n - 1), \
                       min(lcs(m - 1, n), lcs(m, n - 1))) + 1;
    return dp[m][n];
}


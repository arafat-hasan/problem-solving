#include <math.h>
int main() {
    double r,n;
    while(scanf("%lf %lf",&r,&n)==2){
        printf("%.3lf\n",0.5*r*sin(2*3.1415926535897932384626433832795/n)*r*n);
    }

    return 0;
}

struct chem {
    int id, val;
};

bool compare (const chem &a, const chem &b) {
    if (a.id > b.id) return true;
    else if (a.id == b.id) {
        return a.val > b.val;
    }

    return false;
}

int main() {
    _FastIO;
    int n, m, id, val;
    vector<chem> v;
    cin >> n;
    rep (i, n) {
        cin >> id >> val;
        v.pb ({id, val});
    }
    cin >> m;
    rep (i, m) {
        cin >> id >> val;
        v.pb ({id, val});
    }
    sort (all (v), compare);
    set<int> st;
    ll sum = 0;

    for (auto i : v) {
        //debug2 (i.id, i.val);
        if (st.count (i.id) == 0) {
            st.insert (i.id);
            sum += (ll) i.val;
        }
    }

    cout << sum << endl;
    return 0;
}


